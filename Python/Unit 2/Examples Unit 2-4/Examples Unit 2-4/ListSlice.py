# Very similiar to the String slices.
nameList = ['Joe', 'Ed', 'Fred', 'Joe']
list = nameList [0:3]
print (list)
list = nameList [:3]
print (list)
item = nameList [2]
print (item)
list = nameList [2:]
print (list)
list = nameList [-3:] 
print (list)
everyOther = nameList [::2]
print (everyOther)


