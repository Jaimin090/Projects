#DictCreation.py
prices = {'bananas':1.99, 'oranges':1.49}
print(f'The price of bananas is {prices["bananas"]}')
print(f'The price of oranges is {prices["oranges"]}')

sale_prices = dict() # creates empty dictionary, same as {}
sale_prices['bananas'] = 1.29
print(f'The sale price of bananas is {sale_prices["bananas"]}')

