g = 'go FLAMES go'
print ('capitalize method')
print (g.capitalize())
print ('title method')
print (g.title())
print ('lower method')
print (g.lower())
print ('upper method')
print (g.upper())
print ('swapcase method')
print (g.swapcase())
# stripping methods
x = ' Oilers Rule  '        # there are some spaces in the string
print ('lstrip method')  
print (x.lstrip())          # left space omitted
print ('rstrip method')   
print (x.rstrip())          # right spaces omitted
print ('strip method')
print (x.strip())           # all spaces omitted 